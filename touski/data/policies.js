function normalizeLocale(locale) {
  return ["fr", "en", "de", "es"].includes(locale) ? locale : "fr";
}

function getPolicyOrThrow(policyKey) {
  const policy = policies[policyKey];

  if (!policy) {
    throw new Error(`Unknown policy key: ${policyKey}`);
  }

  return policy;
}

export function getPolicyContent(policyKey, locale = "fr") {
  const policy = getPolicyOrThrow(policyKey);
  const safeLocale = normalizeLocale(locale);

  return policy.content[safeLocale] || policy.content.fr;
}

export function getPolicyPath(policyKey, locale = "fr") {
  const policy = getPolicyOrThrow(policyKey);
  const safeLocale = normalizeLocale(locale);
  const prefix = safeLocale === "fr" ? "" : `/${safeLocale}`;

  return `${prefix}/${policy.slug}`;
}

export function getPolicyMetadata(policyKey, locale = "fr") {
  const content = getPolicyContent(policyKey, locale);
  const canonicalPath = getPolicyPath(policyKey, locale);

  return {
    title: `${content.metaTitle} | TOUSKI`,
    description: content.metaDescription,
    alternates: {
      canonical: `${BASE_URL}${canonicalPath}`,
      languages: {
        "fr-CA": `${BASE_URL}${getPolicyPath(policyKey, "fr")}`,
        "en-CA": `${BASE_URL}${getPolicyPath(policyKey, "en")}`,
        "de-DE": `${BASE_URL}${getPolicyPath(policyKey, "de")}`,
        "es-ES": `${BASE_URL}${getPolicyPath(policyKey, "es")}`,
      },
    },
  };
}
